def findZigZagSequence(a, n):
    a.sort()
    #mid = int((n + 1)/2)
    mid = int((n+1)/2) + 1
    a[mid], a[n-1] = a[n-1], a[mid]

    st = mid + 1
    #ed = n - 1
    ed = n - 2
    while(ed <= n-1):
        a[st], a[ed] = a[ed], a[st]
        st = st + 1
        #ed = ed + 1
        ed = ed - 1

    answer = []
    for i in range (n):
        if i == n-1:
            print(a[i])
        else:
            print(a[i], end = ' ')
        answer.append(a[i])
    return answer

