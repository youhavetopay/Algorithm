
'''
    프로그래머스 멀리 뛰기
    
    멀리 뛰기를 할때 1칸, 2칸 씩 갈 수 있으면
    N 번째 칸까지 가는 조합의 수를 구하는 문제
'''

def solution(n):

    '''
        나의 풀이

        나의 접근법
        옛날에 백준에서 이런 비슷한 문제를 풀어본 경험이 있어서
        DP 문제 라는 걸 바로 알았음
        그래서 딱 보니 피보나치랑 똑같아서
        했더니 풀림 ㅋㅋ
    '''

    dp = [0] * (n + 1)
    dp[1] = 1

    if n >= 2:
        dp[2] = 2

    for i in range(3, n+1):
        dp[i] = dp[i-1] + dp[i-2]

    return dp[n] % 1234567

print(solution(2))